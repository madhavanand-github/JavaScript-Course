// COMPARISON OPERATORS: Check if 'true' or 'false'

// '==' equal to 'value'
let a = (5=='5');
console.log(a);

// '===' equal to 'type' and then 'value'
let b = (5==='5');
console.log(b);

// '!=' not equal to 'value'
let c = (5 != '5');
console.log(c);

// '!=' not equal to 'type' and then 'value'
let d = (5 !== '5');
console.log(d);

// '>' Greater than
// '>=' Greater than equal to
// '<'  Less than
// '<=' Less than equal to

